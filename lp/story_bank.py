import random
import datetime

# Format → best theme mapping
FORMAT_THEME_MAP = {
    "A":  ["struggle"],                              # Pain Point
    "B":  ["couple_dynamic", "struggle"],            # Couple Humor
    "BW": ["couple_dynamic", "marriage"],            # Couple Humor + Wisdom
    "C":  ["marriage", "growth", "turning_points"],  # Open Question
    "D":  ["turning_points", "origin", "marriage"],  # Quiet Wisdom
    "E":  ["growth"],                                # Other People's Wins
}


def get_seed_for_format(post_format: str) -> dict:
    """
    Return a random story seed appropriate for the given post format.
    Rotates by day + format so the same seed isn't reused on consecutive days.
    """
    themes = FORMAT_THEME_MAP.get(post_format, ["origin"])
    theme  = random.choice(themes)
    seeds  = STORY_BANK.get(theme, [])
    if not seeds:
        return {}

    # Day-based rotation so it's deterministic per day but varies daily
    day_seed = datetime.date.today().toordinal() + ord(post_format[0])
    seed = seeds[day_seed % len(seeds)]
    return seed


def get_seed_context(post_format: str) -> str:
    """
    Return a formatted string to inject into the Gemini prompt as story context.
    """
    seed = get_seed_for_format(post_format)
    if not seed:
        return ""
    return (
        f"REAL STORY SEED — draw inspiration from this real moment in their journey:\n"
        f"Context: {seed['seed']}\n"
        f"Suggested angle: {seed['angle']}\n"
        f"Important: use this as a starting point, not a script. "
        f"Adapt it to fit the format and hook naturally. "
        f"Never fabricate details beyond what is given."
    )
